  private void btnCalculateActionPerformed(
      java.awt.event.ActionEvent evt) { // GEN-FIRST:event_btnCalculateActionPerformed
    ArrayList<Token> tokens;
    try {
      tokens = BinaryTreeArithmetic.tokenize(textFieldExpr.getText());
    } catch (InvalidCharException ex) {
      JOptionPane.showMessageDialog(
          rootPane, ex, "Caracteres inv√°lidos", JOptionPane.ERROR_MESSAGE);
      return;
    }

    BinaryTree tree = BinaryTreeArithmetic.toBinaryTree(tokens);
    String text =
        new DecimalFormat("#.##########").format(BinaryTreeArithmetic.executeBinaryTree(tree));
    lblResult.setText(text);
  } // GEN-LAST:event_btnCalculateActionPerformed

